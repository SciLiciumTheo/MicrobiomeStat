
#' Alpha Diversity Association Test
#'
#' This function implements an association test for multiple alpha diversity measures. It accepts a data.obj as input and returns a list of tests generated for each alpha diversity index.
#'
#' @param data.obj A list object that includes feature.tab (an OTU table with taxa as rows and samples as columns) and meta.dat (a metadata table with samples as rows and variables as columns).
#' @param alpha.obj A pre-calculated alpha diversity object. If NULL, the function will calculate alpha diversity based on data.obj.
#' @param time.var A string representing the time variable's name in the metadata. The default is NULL.
#' @param alpha.name A character vector with the names of alpha diversity indices to compute. Options include: "shannon", "simpson", "observed_species", "chao1", "ace", and "pielou".
#' @param group.var A string representing the group variable's name in the metadata.
#' @param adj.vars A character vector with the names of adjustment variables in the metadata.
#' @param change.base A logical value indicating whether to change the base level of the group variable. Default is FALSE.
#' @param change.func A function or a string indicating the method to calculate change in diversity indices. Default is "lfc" (log fold change).
#'
#' @return A list containing the association tests for each alpha diversity index.
#'
#' @examples
#' generate_alpha_change_test_pair(
#' data.obj = peerj32.obj,
#' alpha.obj = NULL,
#' time.var = "time",
#' alpha.name = c("shannon", "simpson"),
#' subject.var = "subject",
#' group.var = "group",
#' adj.vars = "sex",
#' change.base = "1"
#' )
#'
#' @export
generate_alpha_change_test_pair <-
  function(data.obj,
           alpha.obj = NULL,
           time.var,
           alpha.name,
           subject.var,
           group.var,
           adj.vars,
           change.base,
           change.func = "lfc") {

    if (is.null(alpha.obj)) {
      if (!is_rarefied(data.obj)) {
        message("Diversity analysis needs rarefaction! Call “mStat_rarefy_data” to rarefy the data!")
        data.obj <- mStat_rarefy_data(data.obj)
      }
      otu_tab <- load_data_obj_count(data.obj)
      alpha.obj <- mStat_calculate_alpha_diversity(x = otu_tab, alpha.name = alpha.name)
    }

    meta_tab <-
      load_data_obj_metadata(data.obj) %>% as.data.frame() %>% select(all_of(c(
        subject.var, group.var, time.var, adj.vars
      )))

    # Convert the alpha.obj list to a data frame
    alpha_df <-
      bind_cols(alpha.obj) %>% bind_cols(tibble("sample" = colnames(otu_tab))) %>%
      inner_join(meta_tab %>% rownames_to_column("sample"),
                 by = c("sample"))

    if (is.null(change.base)){
      change.base <- unique(alpha_df %>% select(all_of(c(time.var))))[1,]
      message("The 'change.base' variable was NULL. It has been set to the first unique value in the 'time.var' column of the 'alpha_df' data frame: ", change.base)
    }

    change.after <-
      unique(alpha_df %>% select(all_of(c(time.var))))[unique(alpha_df %>% select(all_of(c(time.var)))) != change.base]
    # 将 alpha_df 分组，并将其分成一个列表，每个 time 值都有一个独立的 tibble
    alpha_grouped <- alpha_df %>% group_by(time)
    alpha_split <- split(alpha_df, f = alpha_grouped$time)

    # 提取 alpha_split 中的第一个和第二个表
    alpha_time_1 <- alpha_split[[change.base]]
    alpha_time_2 <- alpha_split[[change.after]]

    # 计算多样性指数的变化
    combined_alpha <- alpha_time_1 %>%
      inner_join(
        alpha_time_2,
        by = c(subject.var, group.var),
        suffix = c("_time_1", "_time_2")
      )

    # 使用 lapply 遍历 alpha.name 列表，为每个 alpha.name 计算差值
    diff_columns <- lapply(alpha.name, function(index) {
      # 为每个 alpha.name 创建差值列名
      diff_col_name <- paste0(index, "_diff")

      # 使用 mutate() 函数计算差值，并将其分配给新列
      if (is.function(change.func)) {
        # 如果 change.func 是一个函数，我们将使用它来计算差值
        combined_alpha <- combined_alpha %>%
          mutate(!!diff_col_name := change.func(!!sym(paste0(
            index, "_time_2"
          )), !!sym(paste0(
            index, "_time_1"
          )))) %>%
          select(all_of(diff_col_name))
      } else {
        # 否则，我们假定 change.func 是一个字符向量，我们将根据它的值来计算差值
        if (change.func == "lfc") {
          combined_alpha <- combined_alpha %>%
            mutate(!!diff_col_name := log(!!sym(paste0(
              index, "_time_2"
            )) / !!sym(paste0(
              index, "_time_1"
            )))) %>%
            select(all_of(diff_col_name))
        } else {
          combined_alpha <- combined_alpha %>%
            mutate(!!diff_col_name := !!sym(paste0(index, "_time_2")) -!!sym(paste0(index, "_time_1"))) %>%
            select(all_of(diff_col_name))
        }
      }
    })

    combined_alpha <- bind_cols(combined_alpha, diff_columns)

    # 若有strata.var，连接strata.var列
    if (!is.null(adj.vars)) {
      combined_alpha <-
        combined_alpha %>% left_join(alpha_time_1 %>% select(all_of(c(
          subject.var, adj.vars
        )))
        , by = c(subject.var))
    }

    # Generate tests
    test.list <- lapply(alpha.name, function(index) {

      # Create a formula for lm
      formula <-
        as.formula(paste0(paste0(index, "_diff"), "~", paste(c(
          adj.vars, group.var
        ), collapse = "+")))

      # Run lm and create a coefficient table
      lm.model <- lm(formula, data = combined_alpha)
      coef.tab <- broom::tidy(summary(lm.model))

      # Rearrange the table
      coef.tab <-
        coef.tab %>% select(
          Term = term,
          Estimate = estimate,
          Std.Error = std.error,
          Statistic = statistic,
          P.Value = p.value
        )

      # Run ANOVA on the model if group.var is multi-categorical
      if (length(unique(combined_alpha[[group.var]])) > 2) {
        anova.tab <- broom::tidy(anova(lm.model))

        # Rearrange the table and add missing columns
        anova.tab <- anova.tab %>%
          select(
            Term = term,
            Statistic = statistic,
            df = df,
            P.Value = p.value
          ) %>%
          mutate(Estimate = NA, Std.Error = NA)

        # Reorder the columns to match coef.tab
        anova.tab <- anova.tab %>%
          select(
            Term = term,
            Estimate = Estimate,
            Std.Error = Std.Error,
            Statistic = Statistic,
            P.Value = P.Value
          )

        coef.tab <-
          rbind(coef.tab, anova.tab) # Append the anova.tab to the coef.tab
      }

      return(coef.tab)
    })

    # Assign names to the elements of test.list
    names(test.list) <- alpha.name

    return(test.list)
  }
